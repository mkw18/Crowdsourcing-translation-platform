int Assignment::g_iAssignNum = -1;

/***********************************************************************************************************************
【函数名称】                                 Assignment()
【函数功能】                                 类Assignment的构造函数，在创建对象时被调用
【参数】                                    Translanguage trans, QString title, QString content, QString desc, QString startTime,
                                           QString endTime, int pncpNum,
                                           int transNum, QString pncpDDL, QString transDDL, QString trial, double reward,
                                            Status status, int id，均为输入参数
【返回值】                                   无
【开发者及日期】                              门恺文 2019.7.22
【更改记录】
***********************************************************************************************************************/

Assignment::Assignment(Translanguage trans, QString title, QString content, QString desc, QString startTime,
                       QString endTime, int pncpNum,
                       int transNum, QString pncpDDL, QString transDDL, QString trial, double reward,
                       Status status, int id) :m_tTrans(trans)
{
    m_sTitle = title;
    m_sContent = content;
    m_sDescription = desc;
    m_sStartTime = startTime;
    m_sEndTime = endTime;
    m_iPncpNum = pncpNum;
    m_iTransNum = transNum;
    m_sPncpDDL = pncpDDL;
    m_sTransDDL = transDDL;
    m_sTrial = trial;
    m_dReward = reward;
    m_tTrans = trans;
    m_eStatus = status;
    if (id == -1)
    {
        g_iAssignNum++;
        m_iAssignId = g_iAssignNum;
    }
    else {
        m_iAssignId = id;
        if (id > g_iAssignNum)
        {
            g_iAssignNum = id;
        }
    }
}

