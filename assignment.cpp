/***********************************************************************************************************************
【函数名称】                                 copy()
【函数功能】                                 类的赋值，被复制构造函数、重载=运算符调用
【参数】                                     assign, Assignment, 输入参数
【返回值】                                   assign，复制对象
【开发者及日期】                              门恺文2019.7.22
【更改记录】
***********************************************************************************************************************/

void Assignment::copy(const Assignment &assign)
{
    m_sTitle = assign.m_sTitle;
    m_sContent = assign.m_sContent;
    m_sDescription = assign.m_sDescription;
    m_sStartTime = assign.m_sStartTime;
    m_sEndTime = assign.m_sEndTime;
    m_iPncpNum = assign.m_iPncpNum;
    m_iTransNum = assign.m_iTransNum;
    m_sPncpDDL = assign.m_sPncpDDL;
    m_sTransDDL = assign.m_sTransDDL;
    m_sTrial = assign.m_sTrial;
    m_dReward = assign.m_dReward;
    m_tTrans = assign.m_tTrans;
    m_eStatus = assign.m_eStatus;
    m_iAssignId = assign.m_iAssignId;
    if (assign.m_iAssignId > g_iAssignNum)
    {
        g_iAssignNum = m_iAssignId;
    }
}

